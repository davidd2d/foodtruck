def apply_translations(po_file, translations):
    """Apply translations to a .po file"""
    path = Path(po_file)
    lines = path.read_text(encoding='utf-8').splitlines(keepends=True)
    
    i = 0
    replaced = 0
    
    existing_msgids = set()

    def extract_po_string(start_index, prefix):
        line = lines[start_index]
        if line.startswith(f'{prefix} ""'):
            collected = []
            cursor = start_index + 1
            while cursor < len(lines) and lines[cursor].startswith('"'):
                collected.append(lines[cursor][1:-2])
                cursor += 1
            return ''.join(collected), cursor
        return line[len(prefix) + 2:-2], start_index + 1

    while i < len(lines):
        # Look for msgid
        if lines[i].startswith('msgid "'):
            msgid_text, next_index = extract_po_string(i, 'msgid')
            existing_msgids.add(msgid_text)
            
            # Check if we have a translation for this
            if msgid_text in translations:
                # Next line should be msgstr
                if next_index < len(lines) and lines[next_index].startswith('msgstr "'):
                    current_msgstr, end_index = extract_po_string(next_index, 'msgstr')
                    # Only replace if msgstr is empty
                    if current_msgstr == '':
                        translation = translations[msgid_text]
                        lines[next_index] = f'msgstr "{translation}"\n'
                        del lines[next_index + 1:end_index]
                        replaced += 1
                        next_index = next_index + 1
            i = next_index
            continue
        i += 1
    
    if 'Language: \\n' in ''.join(lines[:20]):
        language_code = 'fr' if po_file.endswith('/fr/LC_MESSAGES/django.po') else 'es'
        joined = ''.join(lines).replace('Language: \\n', f'Language: {language_code}\\n', 1)
        lines = joined.splitlines(keepends=True)

    missing_entries = []
    for msgid_text, translation in translations.items():
        if msgid_text in existing_msgids:
            continue
        missing_entries.append(f'\nmsgid "{msgid_text}"\n')
        missing_entries.append(f'msgstr "{translation}"\n')
        replaced += 1

    if missing_entries:
        lines.extend(missing_entries)

    path.write_text(''.join(lines), encoding='utf-8')
    
    return replaced

